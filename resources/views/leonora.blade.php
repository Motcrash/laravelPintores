<p>Leonora Carrington (Lancashire, Inglaterra, 6 de abril de 1917-Ciudad de México, 25 de mayo de 2011​) fue una pintora surrealista y escritora inglesa nacionalizada mexicana.​​
    Nació el 6 de abril de 1917 en el pueblo de Chorley, en Lancashire, Inglaterra. En 1936 ingresó en la academia Ozenfant de arte, en la ciudad de Londres. Al año siguiente conoció a quien la introdujo indirectamente en el movimiento surrealista: el pintor alemán Max Ernst, a quien volvió a encontrar en un viaje a París y con quien no tardó en establecer una relación sentimental. Durante su estancia en esa ciudad entró en contacto con el movimiento surrealista y convivió con personajes notables del movimiento como Joan Miró y André Breton, así como con otros pintores que se reunían alrededor de la mesa del Café Les Deux Magots, como por ejemplo el pintor Pablo Picasso y Salvador Dalí.</p>
